# Print Diamond with Stars

space = int(input("Enter no. of * to make a diamond - "))

for num_of_star in range( space // 2 ):
    print(" " * ( space // 2 - num_of_star ) , end="")
    print("*" * ( num_of_star * 2 + 1 ))


for num_of_star in range( space // 2 , -1 , -1):
    print(" " * ( space // 2 - num_of_star ) , end="")
    print("*" * ( num_of_star * 2 + 1 ))


